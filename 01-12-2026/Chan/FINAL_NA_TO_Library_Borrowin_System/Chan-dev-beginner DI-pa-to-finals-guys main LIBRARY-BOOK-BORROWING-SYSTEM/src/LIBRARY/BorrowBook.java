    private void issueBttnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_issueBttnActionPerformed
        // TODO add your handling code here:
       Connection con = null;
       PreparedStatement pstInsert = null;
       PreparedStatement pstSelect = null;
       ResultSet rs = null;
       
    
    try {
        // Get a single connection
        con = dataBase.getConnection();
        
        // Get current date
        String borrowedDate = java.time.LocalDate.now().toString();
        
        // 1. First check if a row is selected in the books table
        int selectedRow = Jtable_ShowDisplay.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Please select a book from the table first");
            return;
        }
        
        // Get book details from selected row
        DefaultTableModel booksModel = (DefaultTableModel) Jtable_ShowDisplay.getModel();
        int bookCode = (int) booksModel.getValueAt(selectedRow, 0);
        String bookTitle = (String) booksModel.getValueAt(selectedRow, 1);
        String bookGenre = (String) booksModel.getValueAt(selectedRow, 3);
        
        // Ask for student ID
        String studentIDStr = JOptionPane.showInputDialog(this, "Enter Student ID:");
        if (studentIDStr == null || studentIDStr.trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Student ID is required");
            return;
        }
        
        int studentID;
        try {
            studentID = Integer.parseInt(studentIDStr);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Invalid Student ID. Must be a number.");
            return;
        }
        
        // Generate issue number
        String issueNo = generateIssueNo();
        
        // 2. INSERT new borrow record into transactions table
        // Assuming your transactions table has these columns
        String insertSQL = "INSERT INTO transactions (IssueNo, StudentID, Book_Code, DateIssued) VALUES (?, ?, ?, ?)";
        pstInsert = con.prepareStatement(insertSQL);
        pstInsert.setString(1, issueNo);
        pstInsert.setInt(2, studentID);
        pstInsert.setInt(3, bookCode);
        pstInsert.setString(4, borrowedDate);
        
        int rowsInserted = pstInsert.executeUpdate();
        
        if (rowsInserted > 0) {
            // 3. UPDATE book availability (decrease by 1)
            String updateSQL = "UPDATE books SET Availability = Availability - 1 WHERE Book_Code = ? AND Availability > 0";
            PreparedStatement pstUpdate = con.prepareStatement(updateSQL);
            pstUpdate.setInt(1, bookCode);
            int rowsUpdated = pstUpdate.executeUpdate();
            pstUpdate.close();
            
            if (rowsUpdated > 0) {
                // 4. SELECT and display all transactions
                String selectSQL = "SELECT t.IssueNo, r.StudentID, b.Book_Code, b.Title, b.Genre, t.DateIssued " +
                                   "FROM transactions t " +
                                   "JOIN registertable r ON t.StudentID = r.StudentID " +
                                   "JOIN books b ON t.Book_Code = b.Book_Code " +
                                   "ORDER BY t.DateIssued DESC";
                
                pstSelect = con.prepareStatement(selectSQL);
                rs = pstSelect.executeQuery();
                
                // Clear and populate the BorrowTable
                DefaultTableModel borrowModel = (DefaultTableModel) BorrowTable.getModel();
                borrowModel.setRowCount(0);
                
                while (rs.next()) {
                    borrowModel.addRow(new Object[]{
                        rs.getString("IssueNo"),
                        rs.getString("StudentID"),      // Use getString for consistent display
                        rs.getString("Book_Code"),      // Use getString
                        rs.getString("Title"),
                        rs.getString("Genre"),
                        rs.getString("DateIssued")
                    });
                }
                
                // Show success message with details
                String successMsg = "Book Issued Successfully!\n" +
                                   "Issue No: " + issueNo + "\n" +
                                   "Student ID: " + studentID + "\n" +
                                   "Book: " + bookTitle + "\n" +
                                   "Date: " + borrowedDate;
                JOptionPane.showMessageDialog(this, successMsg);
                
                // Refresh the books table
                loadBooksToTable();
            } else {
                JOptionPane.showMessageDialog(this, "Book is not available for borrowing");
            }
        }
        
    } catch (Exception ex) {
        JOptionPane.showMessageDialog(this, "Database Error: " + ex.getMessage());
        ex.printStackTrace();
    } finally {
        // Close all resources
        try {
            if (rs != null) rs.close();
            if (pstSelect != null) pstSelect.close();
            if (pstInsert != null) pstInsert.close();
            if (con != null) con.close();
        } catch (Exception e) {
            e.printStackTrace();
        } 
    }
        
    }//GEN-LAST:event_issueBttnActionPerformed

