    private void buttonaddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonaddActionPerformed
        // TODO add your handling code here:
         try {
            String sql = "INSERT INTO registertable (StudentID, StudentName, Course_Year, Status, pass) VALUES (?,?,?,?,?)";
            con = (Connection) DriverManager.getConnection("jdbc:mysql://localhost:3306/lbs_jdbc","root","");
            PreparedStatement pst=con.prepareStatement(sql);
            pst.setString(1, textstudentid.getText().trim());
            pst.setString(2, textfullname.getText().trim());
            pst.setString(3, textcourseyr.getText().trim());
            pst.setString(4, textstatus.getText().trim());
            pst.setString(5,textpassword.getText().trim());

            pst.executeUpdate();
            JOptionPane.showMessageDialog(null, "Successfully Added");
            
            loadBooksToTable();
            
        } catch (SQLIntegrityConstraintViolationException e) {
                JOptionPane.showMessageDialog(null, "Account already exists");
        }
          catch (SQLException e) {
                JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_buttonaddActionPerformed

