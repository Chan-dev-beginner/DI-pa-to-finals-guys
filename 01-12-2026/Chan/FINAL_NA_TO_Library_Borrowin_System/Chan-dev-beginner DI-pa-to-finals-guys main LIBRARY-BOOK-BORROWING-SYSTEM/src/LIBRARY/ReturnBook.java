    private void loadBooksToTable() {
        try {
            Connection con = dataBase.getConnection();
            String sql = "SELECT * FROM transactions WHERE StudentID";
            PreparedStatement pst = con.prepareStatement(sql);
            ResultSet rs = pst.executeQuery();

            DefaultTableModel model = (DefaultTableModel) ReturnTable.getModel();
            model.setRowCount(0); // clear table first

            while (rs.next()) {
                model.addRow(new Object[]{
                    rs.getString("IssueNo"),
                    rs.getString("StudentID"),
                    rs.getString("Book_Code"),
                    rs.getString("DateIssued"),
                    rs.getString("DateReturned")
                });
            }

            rs.close();
            pst.close();
            con.close();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error loading data: " + e.getMessage());
            e.printStackTrace();
        }
    }

    
