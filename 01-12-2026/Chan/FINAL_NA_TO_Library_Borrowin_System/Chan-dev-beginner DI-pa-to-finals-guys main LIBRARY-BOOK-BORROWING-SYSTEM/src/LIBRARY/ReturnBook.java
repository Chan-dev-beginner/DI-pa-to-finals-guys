    private void returnbttnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_returnbttnActionPerformed
        // TODO add your handling code here:
        int selectedRow = ReturnTable.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(null, "Please select a book to return from the table!");
            return;
        }
        
        String issueNo = ReturnTable.getValueAt(selectedRow, 0).toString();
        String studentID = ReturnTable.getValueAt(selectedRow, 1).toString();
        String bookCode = ReturnTable.getValueAt(selectedRow, 2).toString();
        
        try {
            java.sql.Date currentDate = new java.sql.Date(new java.util.Date().getTime());
            
            String sql = "UPDATE transactions SET DateReturned = ? WHERE IssueNo = ?";
            con = dataBase.getConnection();
            pst = con.prepareStatement(sql);
            pst.setDate(1, currentDate);
            pst.setString(2, issueNo);
            pst.executeUpdate();
            
            loadBooksToTable();
            JOptionPane.showMessageDialog(null, "Book returned successfully!");
            
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error: " + e.getMessage());
        }
    }//GEN-LAST:event_returnbttnActionPerformed

