    private void issueBttnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_issueBttnActionPerformed
        // TODO add your handling code here:
        issueBttn.addActionListener(e -> {
    try {
        Connection con = dataBase.getConnection();
        
        String issueNo = generateIssueNo();
        
        String BorrowedTime = java.time.LocalDate.now().toString();

        // 1. INSERT new borrow record
        String insertSQL = "INSERT INTO transactions (IssueNo, DateIssued) VALUES (?, ?)";
        PreparedStatement pstInsert = con.prepareStatement(insertSQL);
        pstInsert.setString(1, generateIssueNo());     // Example IssueNo
        pstInsert.setString(2, BorrowedTime);           // Borrowed Time
        pstInsert.executeUpdate();

        // 2. SELECT updated list of borrowed books
        String selectSQL = "SELECT t.IssueNo, r.StudentID, b.Book_Code, b.Title, b.Genre, t.DateIssued " +
                           "FROM transactions t " +
                           "JOIN registertable r ON t.StudentID = r.StudentID " +
                           "JOIN books b ON t.Book_Code = b.Book_Code";

        PreparedStatement pstSelect = con.prepareStatement(selectSQL);
        ResultSet rs = pstSelect.executeQuery();

        DefaultTableModel model = (DefaultTableModel) BorrowTable.getModel();
        model.setRowCount(0); // clear table first
        while(rs.next()) {
            model.addRow(new Object[]{
                rs.getInt("StudentID"),
                rs.getInt("BookCode"),
                rs.getString("IssueNo"),
                rs.getDate("DateIssued")
            });
        }

        JOptionPane.showMessageDialog(null, "Book Issued Successfully!");

        con.close();

    } catch(Exception ex) {
        JOptionPane.showMessageDialog(null, "Database Error: " + ex.getMessage());
    }
    loadBooksToTable();
});
    }//GEN-LAST:event_issueBttnActionPerformed

